			<p>Barlow is a funny guy. Dudley’s got first hand experience of this. Way back in 2003 a friend of Dudley’s who worked in the music industry said, “You’ve got to come and meet these two songwriters I’m working with, they’re like the Two Ronnies, so funny - oh yeah, and one of them’s Gary Barlow.”<br><br>
			Well, this was a long time ago, in what he calls, “the Wilderness Years” when Barlow was writing and producing music but never thought he would perform again. Dudley and GB became sort of chummy, although mostly their friendship consisted of Gary saying funny things and Dudley crying her eyes out laughing like a sap.<br><br>
			Then Take That happened second time around, and Gary had better things to do than hang out with Dudley, things like sell out Wembley Stadium every night for a week in about 10 seconds (OK, maybe 15 minutes), write several multimillion selling hit albums, sing the song that closed the Olympics, and design and host the Queen’s Jubilee concert…to name a few. But he did send her some tickets for their comeback show and despite protesting to everyone (except Gary) that she wasn’t a fan, Dudley became one that very night watching “Gaz” and the lads dance their socks off after 10 years in The Wilderness.<br><br>
			That was the end of that story, Dudley went back to her normal life and Gaz became a pop star again, until one day in 2016 when he rang her and said, “I wanna talk to you about something, can you come over?”<br><br>
			Dudley said, “Can I bring the dog?”<br><br>
			Upon entering Gary Barlow’s home in London, Dudley’s lurcher slouched straight to the bed shared by the family’s two small dogs and settled down for a kip. Barlow didn’t mind, but the canines were a bit miffed, especially the feisty black Pomeranian, Hugo. Then the lurcher got on the sofa. Eek. But that was also cool. The Barlows are dog people. Phew!<br><br>
			Hugo’s addition to the family was inspired by visits from Sharon Osbourne who always had a cute Pom under her arm. In dressing room gatherings after the filming of X Factor, Barlow’s kids loved the Pom as did his wife, Dawn, as he says in his book: “She was giving me those eyes; not the rolling ones, the I want a puppy ones…” A few months later Dawn nipped off up the M6 and found Hugo, the runt of the litter who looked like a tiny fluffy black sheep and behaved like a massive pain in the arse. "It took three years to house train the little bastard and he’s never stopped yapping to this day.”<br><br>
			Cookie is something else. A sweet gentle, loving girl, Cookie - or “Cooksy” as Gary calls her - is a Patterdale terrier x working cocker spaniel x Jack Russell - in his book Gary describes her as, “A peaceful, biddable and loving member of our family. That little Cookie, if she could talk… the secrets I’ve told her coming in after a few wines on a Saturday night. She’s adorable. Unlike Hugo.”<br><br>
			Plans are to tour Music Played by Humans next year but with the world as it is, nothing is certain. While the humans are itching to get back to some kind of normal, Barlow says Cookie has enjoyed the new togetherness of the family pack that Covid has imposed. “No one has enjoyed lockdown more than Cooksy: everyone at home, no suitcases sitting in the hall, no uncertainty, awww, Cooksy. She’s taken to sitting on the back of my chair recently - just being sat beside me is not good enough [See picture]. Of course I love it.” Like so many dogs across the world, “She’ll needs easing back into real life.”</p>
