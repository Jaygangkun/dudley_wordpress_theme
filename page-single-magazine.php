			His first solo album for seven years comes out at the end of November, a beautiful, joyful big band production that sounds very different from the more poppy Take That sound. It’s called, appropriately, Music Played by Humans and features some mega collaborators including Barry Manilow, Michael Bublé, Beverley Knight, the cracking pianist, Chilly Gonzales and a LOLsworthy mutually abusive banter strewn number with his old mate (and not so secret super fan) James Corden. “It’s a funny song that celebrates British humour - in a Northern way!”<br><br>
			“Little did I realise how poignant this album would be in a time now that orchestras can’t sit in a room together. We finished our last session two weeks before lockdown and when I listen to the album now, it brings back great memories. We have some of the best musicians in the world in London...and they’re on my recordings.”
